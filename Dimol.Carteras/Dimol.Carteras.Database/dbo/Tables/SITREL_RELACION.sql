CREATE TABLE [dbo].[SITREL_RELACION] (
    [CODEMP]    INT          NOT NULL,
    [PCLID]     INT          NOT NULL,
    [ACCION]    VARCHAR (20) NOT NULL,
    [CONTACTO]  VARCHAR (20) NOT NULL,
    [RESPUESTA] VARCHAR (20) NOT NULL,
    [ESTADO]    VARCHAR (1)  NULL,
