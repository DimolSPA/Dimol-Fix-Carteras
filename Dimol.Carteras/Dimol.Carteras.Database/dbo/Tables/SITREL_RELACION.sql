    CONSTRAINT [FK_SITREL_RELACION_CONTACTO] FOREIGN KEY ([CODEMP], [PCLID], [CONTACTO]) REFERENCES [dbo].[SITREL_CONTACTO] ([CODEMP], [PCLID], [CODIGO]),
    CONSTRAINT [FK_SITREL_RELACION_RESPUESTA] FOREIGN KEY ([CODEMP], [PCLID], [RESPUESTA]) REFERENCES [dbo].[SITREL_RESPUESTA] ([CODEMP], [PCLID], [CODIGO])
);

